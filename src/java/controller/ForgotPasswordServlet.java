private void sendOTP(String email, int otp) {
    final String senderEmail = "uchihad.saitama@gmail.com";
    final String senderPassword = "031002"; // App Password thay vì mật khẩu Gmail

    Properties props = new Properties();
    props.put("mail.smtp.auth", "true");
    props.put("mail.smtp.starttls.enable", "true");
    props.put("mail.smtp.host", "smtp.gmail.com");
    props.put("mail.smtp.port", "587");

    Session session = Session.getInstance(props, new Authenticator() {
        protected PasswordAuthentication getPasswordAuthentication() {
            return new PasswordAuthentication(senderEmail, senderPassword);
        }
    });

    try {
        Message message = new MimeMessage(session);
        message.setFrom(new InternetAddress(senderEmail));
        message.setRecipients(Message.RecipientType.TO, InternetAddress.parse(email));
        message.setSubject("Mã OTP xác nhận");
        message.setText("Mã OTP của bạn là: " + otp);

        Transport.send(message);
        System.out.println("OTP đã gửi thành công đến: " + email);
    } catch (MessagingException e) {
        e.printStackTrace();
    }
}


